
public class Multiply {

	public static void main(String[] args) {
		int a;	//'a'라는 이름의 정수형 변수선언
		int b;	//'b'라는 이름의 정수형 변수선언
		int result;	//'result'이라는 이름의 정수형 변수선언
		
		a = 50;	//a라는 변수에 50을 넣어줌
		b = 10;	//b라는 변수에 10을 넣어줌
		result = a*b;	//a*b의 값을 result에 넣어줌
		
		System.out.print("a와 b의 곱은");	//""안의 내용을 콘솔에 출력
		System.out.print(result);	//sum의 값을 콘솔에 출력
		System.out.print("입니다.");	//""안의 내용을 콘솔에 출력
		System.out.print("a와 b의 곱은"+result+"입니다.");


	}

}
